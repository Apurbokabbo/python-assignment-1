num_1 = int(input("Enter a number: "))
num_2 = int(input("Enter another number: "))

if num_1 > num_2:
    print(f"The number one {num_1} is greater than the number two {num_2}")
elif num_1 < num_2:
    print(f"The number one {num_1} is less than the number two {num_2}")
else:
    print(f"The number one {num_1} and two {num_2} are equal")